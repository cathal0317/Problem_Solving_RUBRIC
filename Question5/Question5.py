from fractions import Fraction

def Calculate_q0(m: int):
    q = Fraction(1, 1)   
    for r in range(m - 2, -1, -1):
        p = Fraction(r + 1, m)
        q = p + (1 - p) * (1 - q)
    return q

for m in range(2, 15):
    q0 = Calculate_q0(m)
    print(m, q0, float(q0), float(q0 - Fraction(1, 2)))
    print("--------------------------------")


